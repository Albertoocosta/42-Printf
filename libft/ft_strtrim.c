#include "libft.h"

char	*ft_strtrim(char const *s1, char const *set)
{
	int		start;
	int		end;
	char	*result;

	start = 0;
	end = ft_strlen(s1) - 1;
	while (s1[start] && ft_strchr(set, s1[start]))
		start++;
	while (end && ft_strrchr(set, s1[end]))
		end--;
	result = ft_substr(s1, start, (end - start + 1));
	return (result);
}
