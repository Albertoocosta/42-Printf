#include "libftprintf.h"

int	ft_putnb(int nbr)
{
	int	i;
	
	i = 0;
	if (nbr == -2147483648)
	{
		write(1, "-2147483648", 11);
		return (0); 
	}
	if (nbr < 0)
	{
		write(1, "-", 1);
		nbr *= -1;
	}
	if (nbr < 10)
	{
		i = nbr + '0';
		write(1, &i, 1);
	}
	if (nbr >= 10)
	{
		ft_putnb(nbr / 10);
		ft_putnb(nbr % 10);
	}
	return (1);
}
